#include <stdio.h>
 
int main() {

    int A, B, SOMA;              // Declaração de variáveis
    scanf("%d", &A);             // Usuário digita o valor de A
    scanf("%d", &B);             // Usuário digita o valor de B
    SOMA = A + B;                // Realiza o cálculo da soma
    printf("SOMA = %d\n", SOMA); // Retorna o valor para SOMA
    return 0;
}